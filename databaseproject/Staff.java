    public int selectedIndex(String a){
        
        if (a.equals("Male")){
            return 0;
        }
        else if (a.equals("Female")){
            return 1;
        }
        else if (a.equals("Physician")){
            return 0;
        }
        else if (a.equals("Surgeon")){
            return 1;
        }
        else if (a.equals("Nurse")){
            return 2;
        }
        else if (a.equals("SupportStaff")){
            return 3;
        }
        else if (a.equals("0")){
            return 0;
        }
        else if (a.equals("1")){
            return 1;
        }
        else if (a.equals("2")){
            return 2;
        }
        else if (a.equals("3")){
            return 3;
        }
        else if (a.equals("4")){
            return 4;
        }
        else{
        return -1;
        }
    }
