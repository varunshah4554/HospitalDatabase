    public void AutoID(){
        try {
            Statement s = con.createStatement();
            rs = s.executeQuery("select MAX(staffnumber) from Staff");
            rs.next();
            rs.getString("MAX(staffnumber)");
            
            if(rs.getString("MAX(staffnumber)") == null){
                staffnum.setText("1");
            }
            else{
                int id = Integer.parseInt(rs.getString("MAX(staffnumber)"));
                id++;
                staffnum.setText(Integer.toString(id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void staff_table(){
        try {
            pst = con.prepareStatement("select * from Staff");
            rs = pst.executeQuery();
        
            ResultSetMetaData Rsm = rs.getMetaData();
            int c;
            c = Rsm.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for(int i = 1; i <= c; i++){
                v2.add(rs.getString("staffnumber"));
                v2.add(rs.getString("name"));
                v2.add(rs.getString("gender"));
                v2.add(rs.getString("phone"));
                v2.add(rs.getString("ssn"));
                v2.add(rs.getString("address"));
                v2.add(rs.getString("type"));
                v2.add(rs.getString("shift"));
                }
                
                df.addRow(v2);
            }
                    
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
